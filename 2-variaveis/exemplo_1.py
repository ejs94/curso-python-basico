# Este script armazena informações sobre uma máquina na fábrica.

# Associando uma variável ao nome da máquina
nome_maquina = "Fresadora 3000"

# Associando uma variável ao numero de peças produzidas
pecas_produzidas = 150

# Associando uma variável a porcentagem de eficiência da máquina
eficiencia_maquina = 95.5

# Imprimir na terminal os resultados das variáveis
print("Nome da máquina", nome_maquina)
print("Quantidade de peças produzidas:", pecas_produzidas)
print("Eficiência da máquina:", eficiencia_maquina, "%")