# Exemplo para explicar os tipos de variáveis mais comuns.
idade = 30 # Armazena a idade como um número inteiro

potencia = 500.75 # Armazena a potencia de um equipamento com pontos flutuantes.

impedancia = 50 + 30j # Armazena a impedância elétrica com parte real e imaginaria (resistência + reatância).

nome_equipamento = "Transformador" # Armazena o nome de um equipamento por uma cadeia de caracteres.

em_operacao = True # Armazena se o equipamento está em operação (verdadeiro ou falso).

# Exibir os valores e seus tipos.
print("Valor:", idade, "Tipo:", type(idade))
print("Valor:", potencia, "Tipo:", type(potencia))
print("Valor:", impedancia, "Tipo:", type(impedancia))
print("Valor:", nome_equipamento, "Tipo:", type(nome_equipamento))
print("Valor:", em_operacao, "Tipo:", type(em_operacao))