# Escreva uma função que recebe um número, retorne:
#   1. `True`, se for primo.
#   2. `False`, se não for primo.

# DICA:
# - Um número é primo se ele for maior que 1 e não tiver divisores além de 1 e ele mesmo.
# - Para verificar se um número é primo, teste a divisibilidade por todos os números de 2 até (number - 1).
#     Se encontrar algum número que divide `number` exatamente (resto 0), ele não é primo.
# - Caso nenhum divisor seja encontrado, o número é primo.